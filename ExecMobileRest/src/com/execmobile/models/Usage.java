package com.execmobile.models;

/**
 * @author Nakul's Dev Machine
 *
 */
public class Usage {
	
	private String Date;
	private String IMEI;
	private String CountryCode;
	private String CountryName;
	private String WBUsage;
	private String HSUsage;
	private String TotalUsage;
	/**
	 * @return the date
	 */
	public String getDate() {
		return Date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		Date = date;
	}
	/**
	 * @return the iMEI
	 */
	public String getIMEI() {
		return IMEI;
	}
	/**
	 * @param iMEI the iMEI to set
	 */
	public void setIMEI(String iMEI) {
		IMEI = iMEI;
	}
	/**
	 * @return the countryCode
	 */
	public String getCountryCode() {
		return CountryCode;
	}
	/**
	 * @param countryCode the countryCode to set
	 */
	public void setCountryCode(String countryCode) {
		CountryCode = countryCode;
	}
	/**
	 * @return the countryName
	 */
	public String getCountryName() {
		return CountryName;
	}
	/**
	 * @param countryName the countryName to set
	 */
	public void setCountryName(String countryName) {
		CountryName = countryName;
	}
	/**
	 * @return the wBUsage
	 */
	public String getWBUsage() {
		return WBUsage;
	}
	/**
	 * @param wBUsage the wBUsage to set
	 */
	public void setWBUsage(String wBUsage) {
		WBUsage = wBUsage;
	}
	/**
	 * @return the hSUsage
	 */
	public String getHSUsage() {
		return HSUsage;
	}
	/**
	 * @param hSUsage the hSUsage to set
	 */
	public void setHSUsage(String hSUsage) {
		HSUsage = hSUsage;
	}
	/**
	 * @return the totalUsage
	 */
	public String getTotalUsage() {
		return TotalUsage;
	}
	/**
	 * @param totalUsage the totalUsage to set
	 */
	public void setTotalUsage(String totalUsage) {
		TotalUsage = totalUsage;
	}
	
	

}
